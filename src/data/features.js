// Total embedded value for a specific license (per user per month)
export const calculateLicenseEmbeddedValue = (licenseId) => {
  return featureCategories.reduce((total, category) => {
    return total + category.features.reduce((catTotal, feature) => {
      return feature.includedIn.includes(licenseId)
        ? catTotal + feature.defaultCost
        : catTotal
    }, 0)
  }, 0)
}
